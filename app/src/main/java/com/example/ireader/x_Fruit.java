package com.example.ireader;
public class x_Fruit {
    private String name;
    private int imageId;

    public x_Fruit(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }
}

